package controlAndCntrolflowStatement;

public class If_Statement {
    static void main(String[] args) {
        //if Runs than 50 then print Good player
        int run = 80;
        if (run > 50) {
            System.out.println("Good Player");
        }
        System.out.println("======");
        if (run < 50) {
            System.out.println("Good Player");
        }
        System.out.println("======");
        if (run >= 50) {
            System.out.println("Good Player");
        }
        System.out.println("======");
        if (run <= 50) {
            System.out.println("Good Player");
        }
        System.out.println("======");
        if (run != 50) {
            System.out.println("Good Player");
        }
    }
}
