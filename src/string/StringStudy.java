package string;

public class StringStudy {
    static void main(String[] args) {
        //there is two way to create a string

        String city="pune";     //1st way
        //string is inbuilt class  of java this class is final class
        //string is a class and city is instance variable(object)
        System.out.println(city);

        //2nd way

        String country=new String("India");  //2nd way
        //string is class name country is object
        //new is memory assign operator
        System.out.println(country);




    }
}
