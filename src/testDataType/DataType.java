package testDataType;

public class DataType {
    static void main(String[] args) {
        //store no  with wickets
        byte wicket; //variable assigned
        wicket =8;  // variable declare
        System.out.println(wicket); //print value
        System.out.println("wicket"); //print wicket ""
        System.out.println("wickets are"+wicket); //with string
        System.out.println("wickets are "+wicket); //with space

        //store RollNo with int
        int RollNo;
        RollNo =5;
        System.out.println(RollNo);
        System.out.println("RollNo is "+RollNo);

        //store decimal no with weight
        float weight;
        weight =62.5f; //f is mandatory to declare decimal no with float
        System.out.println("My  weight is "+weight);

        // store decimal no with gold_weight
        double gold_weight;
        gold_weight =10.5d; //d is mandatory to declare decimal no with float
        System.out.println("My Gold weight is "+gold_weight);

        //store single digit character with char
        char div;
        div ='A';
        System.out.println("My div is "+div);

        //store result status with boolean
        boolean result;
        result = true;
       System.out.println("My result is "+result);




    }
}
